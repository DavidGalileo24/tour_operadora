<?php 
defined('BASEPATH') OR exit('No direct script access allowed');

class usuario_controller extends CI_Controller{
	public function __construct(){
		parent:: __construct();
		$this->load->model('usuario_model');
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//
	public function index(){
		$data = array('title' => 'Usuarios YT');
		$this->load->view('template/head',$data);
		$this->load->view('template/menu_admin');		
		$this->load->view('usuario_view');
		$this->load->view('template/footer');
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//
	public function mostrar_usuario(){
		$respuesta = $this->usuario_model->get_usuario();
		echo json_encode($respuesta);
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//
//----------------------------------------------------------------------------------------------------------------------------------------------//
	public function eliminar(){
		$id = $this->input->post('id');
		$respuesta = $this->usuario_model->delete($id);
		echo json_encode($respuesta);
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//
//----------------------------------------------------------------------------------------------------------------------------------------------//
	public function mostrar_empleado(){
		$respuesta = $this->usuario_model->get_empleado();
		echo json_encode($respuesta);
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//
	public function mostrar_rol(){
		$respuesta = $this->usuario_model->get_rol();
		echo json_encode($respuesta);
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//
//----------------------------------------------------------------------------------------------------------------------------------------------//
	public function ingresar(){
		$datos['empleado'] = $this->input->post('cmb_empleado');
		$datos['usuario'] = $this->input->post('usuario');
		$datos['clave'] = $this->input->post('clave');
		$datos['rol'] = $this->input->post('cmb_rol');

		$respuesta = $this->usuario_model->set_datos($datos);

		echo json_decode($respuesta);
	}
//----------------------------------------------------------------------------------------------------------------------------------------------//

